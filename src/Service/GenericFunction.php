	public function getUniqCleanNameForFile($file)
	{
		$file = preg_replace('/[^A-Za-z0-9 _\-.]/', '', $file->getClientOriginalName());
		return uniqid()."_".$file;
	}

	public function file_get_contents_proxy($url, $proxy)
	{
		$cu = curl_init();

		curl_setopt($cu, CURLOPT_URL, $url);
		curl_setopt($cu, CURLOPT_PROXY, $proxy);
		curl_setopt($cu, CURLOPT_FOLLOWLOCATION, 1);
		curl_setopt($cu, CURLOPT_RETURNTRANSFER, 1);
		curl_setopt($cu, CURLOPT_HEADER, 0);

		$curl_scraped_page = curl_exec($cu);

		curl_close($cu);

		return $curl_scraped_page;
	}

	public function getContentURL($url, $proxy = null)
	{
		$proxyArray = explode(":", $proxy);
		$curl = curl_init();
		$timeout = 30;
		curl_setopt($curl, CURLOPT_SSL_VERIFYPEER, FALSE);
		curl_setopt($curl, CURLOPT_HEADER, false);
		curl_setopt($curl, CURLOPT_FOLLOWLOCATION, true);
		curl_setopt($curl, CURLOPT_URL, $url);
		curl_setopt($curl, CURLOPT_REFERER, $url);
		curl_setopt($curl, CURLOPT_RETURNTRANSFER, TRUE);
		curl_setopt($curl, CURLOPT_USERAGENT, "Mozilla/5.0 (Windows; U; Windows NT 6.1; en-US) AppleWebKit/533.4 (KHTML, like Gecko) Chrome/5.0.375.125 Safari/533.4");
		
		if(!empty($proxy))
		{
			curl_setopt($curl, CURLOPT_PROXY, $proxyArray[0]);
			curl_setopt($curl, CURLOPT_PROXYPORT, $proxyArray[1]);
		}

		$str = curl_exec($curl);
		curl_close($curl);
		
		return $str;
	}
	
	public function getContentDom($url, $ipProxy = null)
	{
		if(!empty($ipProxy))
			$html = $this->getContentURL($url, $ipProxy);
		else
			$html = $this->getContentURL($url);

		$dom = new \simple_html_dom();
		$dom->load($html);
		
		return $dom;
	}

	public static function slugify($text, $max_size = null)
	{
		$text = html_entity_decode($text, ENT_QUOTES);
		$text = mb_detect_encoding($text) == "UTF-8" ? $text : utf8_encode($text);
		$text = GenericFunction::removeAccents($text);

		// replace non letter or digits by -
		$text = preg_replace('~[^\pL\d]+~u', '-', $text);

		// transliterate
		$text = iconv('utf-8', 'us-ascii//TRANSLIT', $text);

		// remove unwanted characters
		$text = preg_replace('~[^-\w]+~', '', $text);

		// trim
		$text = trim($text, '-');

		// remove duplicate -
		$text = preg_replace('~-+~', '-', $text);

		// lowercase
		$text = strtolower($text);

		if (empty($text)) {
			return 'n-a';
		}

		if(!empty($max_size))
			return trim(substr($text, 0, $max_size), "-");

		return $text;
	}

	public function adaptImageSize($file, $width=null)
	{
		if(!file_exists($file) or !is_file($file))
			$file = "photo/640px-Starry_Night_Over_the_Rhone.jpg";
		
		if(empty($width))
			return $file;

		$newLarg = 0.0;
		$newLong = 0.0;
		
		$info = getimagesize($file);
		$eX = 0.0;
		
		if($info[0] > $width)
		{
			$eX = $width / $info[0];
			$newLarg = $eX * $info[0];
			$newLong = $eX * $info[1];
		}
		else
		{
			$newLarg = $info[0];
			$newLong = $info[1];
		}
		
		return array($newLarg, $newLong, $file);
	}
	
